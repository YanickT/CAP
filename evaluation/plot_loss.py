def plot(l, name):
    c = 0
    last = None
    for lr, loss in l:
        length = len(loss)
        if last is None:
            plt.plot(list(range(c, c + length)), loss, "x-", label=f"lr = {lr}")
        else:
            plt.plot(list(range(c - 1, c + length)), [last] + loss, "x-", label=f"lr = {lr}")
        c += length
        last = loss[-1]

    plt.xlabel("Epoch")
    plt.ylabel("Loss")
    plt.title(f"Training for the {name}")
    plt.grid()
    plt.legend()
    plt.show()

